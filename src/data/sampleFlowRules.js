// Flow rule verilerini analiz etmek için yardımcı fonksiyonlar
export const analyzeFlowRules = (data) => {
  const processes = new Map();
  const channels = new Set();
  const applications = new Set();
  const modules = new Set();
  
  data.forEach(rule => {
    // Process bilgilerini topla
    if (!processes.has(rule.NPROCESS_ID)) {
      processes.set(rule.NPROCESS_ID, {
        id: rule.NPROCESS_ID,
        description: rule.VPROCESS_DESC,
        channels: new Set(),
        applications: new Set(),
        modules: new Set(),
        successTargets: new Set(),
        failTargets: new Set()
      });
    }
    
    const process = processes.get(rule.NPROCESS_ID);
    process.channels.add(rule.NCHANNEL_REF);
    process.applications.add(rule.NAPPLICATION_REF);
    process.modules.add(rule.NAPPLICATION_MODULE_REF);
    
    if (rule.NSUCCESS_PROCESS_ID && rule.NSUCCESS_PROCESS_ID !== rule.NPROCESS_ID) {
      process.successTargets.add(rule.NSUCCESS_PROCESS_ID);
    }
    
    if (rule.NFAILED_PROCESS_ID && rule.NFAILED_PROCESS_ID !== rule.NPROCESS_ID) {
      process.failTargets.add(rule.NFAILED_PROCESS_ID);
    }
    
    // Toplam istatistikler
    channels.add(rule.NCHANNEL_REF);
    applications.add(rule.NAPPLICATION_REF);
    modules.add(rule.NAPPLICATION_MODULE_REF);
  });
  
  return {
    processes: Array.from(processes.values()),
    totalChannels: channels.size,
    totalApplications: applications.size,
    totalModules: modules.size,
    totalRules: data.length
  };
};

